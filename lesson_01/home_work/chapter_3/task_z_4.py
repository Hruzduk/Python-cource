# Запрашиваем введение арабской цифры
numer = int(input("Введите число в диапазоне от 1 до 10  "))
if numer == 1:
    print("I")
elif numer == 2:
    print("II")
elif numer == 3:
    print("III")
elif numer == 4:
    print("IV")
elif numer == 5:
    print("V")
elif numer == 6:
    print("VI")
elif numer == 7:
    print("VII")
elif numer == 8:
    print("VIII")
elif numer == 9:
    print("IX")
elif numer == 9:
    print("X")
else:
    print("Вы ввели не верное значение")