day = int(input('Введите число от 1 до 7  '))
if day == 1:
    print('Понедельник')
elif day == 2:
    print('Вторник')
elif day == 3:
    print('Среда')
elif day == 4:
    print('Четверг')
elif day == 5:
    print('Пятнциа')
elif day == 6:
    print('Суббота')
elif day == 7:
    print('Воскресение')
else:
    print("Вы ввели не верное значение")